-- (0) TURN OFF CASE SENSITIVITY
USE ROLE ACCOUNTADMIN;

ALTER ACCOUNT SET QUOTED_IDENTIFIERS_IGNORE_CASE = TRUE;

-- (1) CREATE DATABASES
USE ROLE SYSADMIN;
CREATE DATABASE "RAW";
CREATE DATABASE "ANALYTICS";
CREATE DATABASE "DBT_DEV";

-- (2) CREATE WAREHOUSES
CREATE WAREHOUSE "LOADING"
WITH WAREHOUSE_SIZE = 'XSMALL'
WAREHOUSE_TYPE = 'STANDARD'
AUTO_SUSPEND = 60
AUTO_RESUME = TRUE
MIN_CLUSTER_COUNT = 1 MAX_CLUSTER_COUNT = 1
SCALING_POLICY = 'STANDARD';

CREATE WAREHOUSE "TRANSFORMING"
WITH WAREHOUSE_SIZE = 'XSMALL'
WAREHOUSE_TYPE = 'STANDARD'
AUTO_SUSPEND = 60
AUTO_RESUME = TRUE
MIN_CLUSTER_COUNT = 1 MAX_CLUSTER_COUNT = 1
SCALING_POLICY = 'STANDARD';

CREATE WAREHOUSE "REPORTING"
WITH WAREHOUSE_SIZE = 'XSMALL'
WAREHOUSE_TYPE = 'STANDARD'
AUTO_SUSPEND = 60
AUTO_RESUME = TRUE
MIN_CLUSTER_COUNT = 1 MAX_CLUSTER_COUNT = 1
SCALING_POLICY = 'STANDARD';

-- (3) CREATE ROLES AND GRANT PRIVILEGES
USE ROLE ACCOUNTADMIN;

CREATE ROLE "LOADER";
CREATE ROLE "TRANSFORMER";
CREATE ROLE "REPORTER";


GRANT ALL PRIVILEGES ON WAREHOUSE "RAW" TO ROLE "LOADER";
GRANT ALL PRIVILEGES ON WAREHOUSE "DBT_DEV" TO ROLE "TRANSFORMER";
GRANT ALL PRIVILEGES ON WAREHOUSE "ANALYTICS" TO ROLE "REPORTER";

GRANT CREATE SCHEMA, MODIFY, MONITOR, USAGE ON DATABASE "RAW" TO ROLE "LOADER";
GRANT USAGE ON DATABASE "RAW" TO ROLE "TRANSFORMER";

GRANT CREATE SCHEMA, MODIFY, MONITOR, USAGE ON DATABASE "DBT_DEV" TO ROLE "TRANSFORMER";
GRANT USAGE ON DATABASE "REPORTER" TO ROLE "TRANSFORMER";

GRANT CREATE SCHEMA, MODIFY, MONITOR, USAGE ON DATABASE "DW_DEV" TO ROLE "REPORTER";
GRANT USAGE ON DATABASE "DW_DEV" TO ROLE "BI_TOOLS_ROLE";

-- (4) CREATE USERS
CREATE USER "TEST_REPORTER"
  MUST_CHANGE_PASSWORD = TRUE
  DEFAULT_ROLE = "REPORTER"
  DEFAULT_WAREHOUSE = "REPORTING"
  PASSWORD = 'PASSWORD'; -- Single quote!
GRANT ROLE "REPORTER" TO USER "TEST_REPORTER";

CREATE USER "TEST_TRANSFORMER"
  MUST_CHANGE_PASSWORD = TRUE
  DEFAULT_ROLE = "TRANSFORMER"
  DEFAULT_WAREHOUSE = "TRANSFORMING"
  PASSWORD = 'PASSWORD'; -- Single quote!
GRANT ROLE "TRANSFORMER" TO USER "TEST_TRANSFORMER";

CREATE USER "TEST_LOADER"
  MUST_CHANGE_PASSWORD = TRUE
  DEFAULT_ROLE = "LOADER"
  DEFAULT_WAREHOUSE = "LOADING"
  PASSWORD = 'PASSWORD'; -- Single quote!
GRANT ROLE "LOADER" TO USER "TEST_LOADER";



USE ROLE SYSADMIN;
